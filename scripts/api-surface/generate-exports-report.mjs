import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const root = process.cwd();
const outputPath = path.join(root, 'docs', 'architecture', 'generated', 'package-exports.md');

const entries = await readdir(path.join(root, 'packages'), { withFileTypes: true });
const reports = [];

for (const entry of entries) {
  if (!entry.isDirectory()) {
    continue;
  }

  const packageDir = path.join(root, 'packages', entry.name);
  const pkg = JSON.parse(await readFile(path.join(packageDir, 'package.json'), 'utf8'));
  reports.push({
    name: pkg.name,
    exports: pkg.exports ?? {},
    main: pkg.main ?? null,
    types: pkg.types ?? null,
  });
}

const lines = [
  `<!-- Generated: ${new Date().toISOString()} -->`,
  '# Package Exports',
  '',
  'Generated by `scripts/api-surface/generate-exports-report.mjs`.',
  '',
];

for (const report of reports.sort((a, b) => a.name.localeCompare(b.name))) {
  lines.push(`## ${report.name}`);
  lines.push('');
  if (report.main) {
    lines.push(`- main: \`${report.main}\``);
  }
  if (report.types) {
    lines.push(`- types: \`${report.types}\``);
  }
  lines.push('- exports:');
  const keys = Object.keys(report.exports);
  if (!keys.length) {
    lines.push('  - none');
  } else {
    for (const key of keys.sort()) {
      lines.push(`  - \`${key}\``);
    }
  }
  lines.push('');
}

await writeFile(outputPath, `${lines.join('\n')}\n`, 'utf8');
