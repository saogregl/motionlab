import { readdir, writeFile } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const outputPath = path.join(root, "docs", "architecture", "generated", "repo-tree.md");
const sections = [
  { label: "apps", dir: "apps" },
  { label: "packages", dir: "packages" },
  { label: "native", dir: "native" },
  { label: "schemas", dir: "schemas" },
  { label: "docs", dir: "docs" },
];

async function listTopLevel(dir) {
  const entries = await readdir(path.join(root, dir), { withFileTypes: true });
  return entries
    .filter((entry) => entry.isDirectory())
    .map((entry) => `- \`${dir}/${entry.name}\``)
    .sort();
}

const lines = ["# Repo Tree", "", "Generated by `scripts/repo-map/generate-repo-tree.mjs`.", ""];

for (const section of sections) {
  lines.push(`## ${section.label}`);
  lines.push("");
  const items = await listTopLevel(section.dir);
  lines.push(...items);
  lines.push("");
}

await writeFile(outputPath, `${lines.join("\n")}\n`, "utf8");
