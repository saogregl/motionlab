import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const root = process.cwd();
const workspaces = ['apps', 'packages'];
const outputPath = path.join(
  root,
  'docs',
  'architecture',
  'generated',
  'workspace-dependencies.md',
);

async function collectWorkspacePackages(baseDir) {
  const entries = await readdir(path.join(root, baseDir), { withFileTypes: true });
  const packages = [];

  for (const entry of entries) {
    if (!entry.isDirectory()) {
      continue;
    }

    const dir = path.join(root, baseDir, entry.name);
    const pkg = JSON.parse(await readFile(path.join(dir, 'package.json'), 'utf8'));
    const deps = {
      ...pkg.dependencies,
      ...pkg.devDependencies,
      ...pkg.peerDependencies,
    };
    const workspaceDeps = Object.keys(deps)
      .filter((dep) => dep.startsWith('@motionlab/'))
      .sort();
    packages.push({
      name: pkg.name,
      location: `${baseDir}/${entry.name}`,
      workspaceDeps,
    });
  }

  return packages;
}

const allPackages = (await Promise.all(workspaces.map(collectWorkspacePackages))).flat();
const lines = [
  `<!-- Generated: ${new Date().toISOString()} -->`,
  '# Workspace Dependencies',
  '',
  'Generated by `scripts/dependency-graph/generate-workspace-graph.mjs`.',
  '',
];

for (const pkg of allPackages.sort((a, b) => a.name.localeCompare(b.name))) {
  lines.push(`## ${pkg.name}`);
  lines.push('');
  lines.push(`- Location: \`${pkg.location}\``);
  lines.push(
    `- Workspace dependencies: ${pkg.workspaceDeps.length ? pkg.workspaceDeps.map((dep) => `\`${dep}\``).join(', ') : 'none'}`,
  );
  lines.push('');
}

await writeFile(outputPath, `${lines.join('\n')}\n`, 'utf8');
